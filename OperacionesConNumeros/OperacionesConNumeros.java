package OperacionesConNumeros;

public class OperacionesConNumeros {
    int suma;
    int suma2;
    int nums;
    int nums2;

    int numero;

    int resp;
    int resp2;

    public OperacionesConNumeros(int suma, int suma2, int nums, int nums2, int numero, int resp, int resp2) {
        this.suma = suma;
        this.suma2 = suma2;
        this.nums = nums;
        this.nums2 = nums2;
        this.numero = numero;
        this.resp = resp;
        this.resp2 = resp2;
    }


    public int sumandonums(){
        for(int i=0; i<resp; i++){
            suma += nums;
        }
        return numero;
}
    
    
}
